package com.example.androidapp_1;

public class Deal {
    private String name;
    private String url;
    private String content;
    private String location;
    private String links;
    private double price;
    private String benefits;
    private String image;

    // Constructor, getters, and setters

    public Deal(String name, String url, String content, String location, String links, double price, String benefits, String image) {
        this.name = name;
        this.url = url;
        this.content = content;
        this.location = location;
        this.links = links;
        this.price = price;
        this.benefits = benefits;
        this.image = image;
    }

    // Getters and setters
}
